    public static AppLocalizer Instance { get; } = new();

    private readonly Dictionary<string, Dictionary<string, string>> _translations =
        new(StringComparer.OrdinalIgnoreCase)
        {
            ["vi-VN"] = BuildVietnamese(),
            ["en-US"] = BuildEnglish(),
            ["zh-CN"] = BuildChinese(),
            ["ja-JP"] = BuildJapanese(),
            ["ko-KR"] = BuildKorean(),
            ["fr-FR"] = BuildFrench()
        };

    private string _language = "vi-VN";

    public event PropertyChangedEventHandler? PropertyChanged;

    public string CurrentLanguage => _language;

    public string this[string key] => Translate(key);

    public void SetLanguage(string? languageCode)
    {
        var normalized = NormalizeLanguage(languageCode);
        if (string.Equals(_language, normalized, StringComparison.OrdinalIgnoreCase))
            return;

        _language = normalized;
        OnPropertyChanged(null);
    }

    public string Translate(string key, string? fallback = null)
    {
        if (_translations.TryGetValue(_language, out var languageMap) &&
            languageMap.TryGetValue(key, out var translated))
        {
            return translated;
        }

        if (_translations.TryGetValue("en-US", out var englishMap) &&
            englishMap.TryGetValue(key, out translated))
        {
            return translated;
        }

        return fallback ?? key;
    }

    public string TranslateCategory(string? category)
    {
        if (string.IsNullOrWhiteSpace(category))
            return Translate("category_shopping");

        return category.Trim().ToLowerInvariant() switch
        {
            "hải sản & ốc" or "hai san & oc" or "seafood & snails" or "seafood" => Translate("category_tourism"),
            "ăn vặt" or "an vat" or "snacks" or "snack" => Translate("category_service"),
            "lẩu & nướng" or "lau & nuong" or "hotpot & grill" or "hotpot" or "grill" => Translate("category_food"),
            "nhậu" or "nhau" or "drinking" or "pub" => Translate("category_entertainment"),
            "giải khát" or "giai khat" or "beverage" or "beverages" or "drinks" => Translate("category_drinks"),
            "ăn no" or "an no" or "hearty meals" or "main meal" => Translate("category_shopping"),

            // Backward compatibility for old category names
            "du lịch" or "tourism" => Translate("category_tourism"),
            "dịch vụ" or "service" or "services" => Translate("category_service"),
            "ăn uống" or "food" or "food & drink" => Translate("category_food"),
            "giải trí" or "entertainment" => Translate("category_entertainment"),
            "mua sắm" or "shopping" or "khác" or "other" => Translate("category_shopping"),
            _ => category
        };
    }

    public string TranslateLanguageName(string? languageCode)
    {
        return languageCode?.ToLowerInvariant() switch
        {
            "vi-vn" => Translate("language_vietnamese"),
            "en-us" => Translate("language_english"),
            "zh-cn" => Translate("language_chinese"),
            "ja-jp" => Translate("language_japanese"),
            "ko-kr" => Translate("language_korean"),
            "fr-fr" => Translate("language_french"),
            _ => languageCode ?? Translate("language_vietnamese")
        };
    }

    private static string NormalizeLanguage(string? languageCode)
    {
        if (string.IsNullOrWhiteSpace(languageCode))
            return "vi-VN";

        languageCode = languageCode.Trim();
        if (languageCode.StartsWith("vi", StringComparison.OrdinalIgnoreCase))
            return "vi-VN";
        if (languageCode.StartsWith("en", StringComparison.OrdinalIgnoreCase))
            return "en-US";
        if (languageCode.StartsWith("zh", StringComparison.OrdinalIgnoreCase))
            return "zh-CN";
        if (languageCode.StartsWith("ja", StringComparison.OrdinalIgnoreCase))
            return "ja-JP";
        if (languageCode.StartsWith("ko", StringComparison.OrdinalIgnoreCase))
            return "ko-KR";
        if (languageCode.StartsWith("fr", StringComparison.OrdinalIgnoreCase))
            return "fr-FR";

        return "en-US";
    }

    private void OnPropertyChanged([CallerMemberName] string? propertyName = null)
    {
        PropertyChanged?.Invoke(this, new PropertyChangedEventArgs(propertyName));
    }

